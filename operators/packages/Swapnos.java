package operators.packages;

public class Swapnos {

	public static void main(String[] args) {
		// Program for Swapping of nos.
		
		int a=20;
		int b=10;
		int temp;
		System.out.println("Original Values of A   "+a+"   Original Value of B   "+b);
		temp=a;
		a=b;
		b=temp;
		
		System.out.println("Changed Values of A   "+a+"   Changed Value of B   "+b);


	}

}
